    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField1.getText());;
        String eyecolor = (String) jComboBox2.getSelectedItem();
        String haircolor = (String) jComboBox1.getSelectedItem();
        String facedesc = (String) jComboBox3.getSelectedItem();
        String otherdetail = jTextArea1.getText();
        if (DaoCriminalData.checkData(id)) {
            if (!DaoCriminalImgDetail.checkData(id)) {
                int i = DaoCriminalImgDetail.save(id, eyecolor, haircolor, facedesc, otherdetail);
                if (i >= 0) {
                    JOptionPane.showMessageDialog(CriminalImgDetailInsert.this, "Data Added successfully!");
                    frame.dispose();
                } else {
                    JOptionPane.showMessageDialog(CriminalImgDetailInsert.this, "Sorry, unable to Add!");
                }
            } else {
                int i = DaoCriminalImgDetail.update(id, eyecolor, haircolor, facedesc, otherdetail);
                if (i >= 0) {
                    JOptionPane.showMessageDialog(CriminalImgDetailInsert.this, "Data Updated successfully!");
                    WelcomeAdmin.main(new String[]{});
                    frame.dispose();
                } else {
                    JOptionPane.showMessageDialog(CriminalImgDetailInsert.this, "Sorry, unable to Update!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(CriminalImgDetailInsert.this, "Sorry, Record Not Found in CriminalData");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

