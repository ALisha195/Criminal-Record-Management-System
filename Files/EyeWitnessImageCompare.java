    public void addImagesFromDatabase() {
        Connection con = DB.getConnection();
        String query1 = "SELECT criminalimage FROM criminaldata";
        Statement st;
        ResultSet rs;

        try {
            st = con.createStatement();
            rs = st.executeQuery(query1);

            while (rs.next()) {
                images.add(rs.getString(1));
            }
        } catch (SQLException e) {
            Logger.getLogger(CriminalDataRetrieveAll.class.getName()).log(Level.SEVERE, null, e);
        }

        System.out.println(images);

    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            upload_image.setIcon(icr.ResizeImage(path, null, upload_image.getWidth(), upload_image.getHeight()));
            imgpath = path;

            System.out.println(imgpath);
        } else {
            System.out.println("No File Selected");
        }
        System.out.println("imgpath = " + imgpath);
    }//GEN-LAST:event_jButton2ActionPerformed

