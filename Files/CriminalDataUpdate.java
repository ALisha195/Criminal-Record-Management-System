    private void FormSubmittionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FormSubmittionActionPerformed
        int criminalid = Integer.parseInt(FCriminalId.getText());
        String firstname = Ffirstname.getText();
        String lastname = FLastname.getText();
        String middlename = Fmiddlename.getText();
        String dob = Fdob.getText();
        String gender = Bmale.isSelected() ? "Male" : "Female";
        int age = Integer.parseInt(Fage.getText());
        String city = Fcity.getText();
        String state = Fstate.getText();
        String dateofarrest = Ddoarrest.getText();
        String crimeinvolved = Fcrimeinvolve.getText();
        int eyewitnessid = Integer.parseInt(Feyeid.getText());
        String eyewitnessname = Feyename.getText();
        int complaintno = Integer.parseInt(Fcomplaintid.getText());
        String criminaladdress = Acriminaladdress.getText();
        String eyewitnessaddress = Feyeaddress.getText();
        String criminalImage=imgpath;
        
        if (DaoCriminalData.checkData(criminalid)) {
            int i = DaoCriminalData.update(criminalid, firstname, lastname, middlename, dob, gender, age,
                    criminaladdress, city, state, dateofarrest, crimeinvolved, eyewitnessid, eyewitnessname,
                    eyewitnessaddress, complaintno,criminalImage);
            
            
            if (i >= 0) {
                JOptionPane.showMessageDialog(CriminalDataUpdate.this, "Data Updated successfully!");
                frame.dispose();
            } else {
                JOptionPane.showMessageDialog(CriminalDataUpdate.this, "Sorry, unable to Update!");
            }
        } else {
            JOptionPane.showMessageDialog(CriminalDataUpdate.this, "Sorry, Data on this Criminal id did'nt exist ");
        }
    }//GEN-LAST:event_FormSubmittionActionPerformed

