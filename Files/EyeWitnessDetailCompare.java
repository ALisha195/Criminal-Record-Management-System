    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String id = "";
        String fname = "";
        String pic = "";
        int len = Math.max(Math.max(Math.max(h.length(), f.length()), e.length()),
                Math.max(Math.max(n.length(), m.length()), c.length()));
        int flag = 0;
        for (int i = 0; i < len; i++) {
            if (c.charAt(i) != '_') {
                if (flag == 0 && h.charAt(i) == f.charAt(i)
                        && f.charAt(i) == e.charAt(i)
                        && e.charAt(i) == n.charAt(i)
                        && n.charAt(i) == m.charAt(i)
                        && m.charAt(i) == c.charAt(i)
                        && c.charAt(i) == h.charAt(i)) {
                    pic += h.charAt(i);
                } else {
                    flag = 2;
                    break;
                }
            } else {
                flag = 1;
                break;
            }

        }

        if (flag == 2) {
            jLabel2.setIcon(icr.ResizeImage("", null, jLabel2.getWidth(), jLabel2.getHeight()));
            System.out.println("Not Found");
        }
        String rev = "";
        if (flag == 1) {
            for (int i = pic.length() - 1; i >= 0; i--) {
                if (pic.charAt(i) == '/') {
                    break;
                }
                rev += pic.charAt(i);
            }
            rev = new StringBuffer(rev).reverse().toString();
            System.out.println(rev);
            flag = 0;
            for (int i = 0; i < rev.length(); i++) {
                if (flag == 0 && Character.isDigit(rev.charAt(i))) {
                    id += rev.charAt(i);
                } else {
                    flag = 1;
                    fname += rev.charAt(i);
                }
            }

            System.out.println(id + "   " + fname);
            pic += ".png";
            jLabel18.setText("Record Found");
            jLabel3.setText("ID:");
            jLabel4.setText("FIRST NAME:");
            jLabel2.setIcon(icr.ResizeImage(pic, null, jLabel2.getWidth(), jLabel2.getHeight()));
            jLabel5.setText(id);
            jLabel6.setText(fname);
            System.out.println("Found");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

