    public int CheckCompare(String header, String data) {
        int output = 0;
        if (header.equals("Select")) {
            return output;
        } else {
            if (data == null || data.trim().equals("")) {
                JOptionPane.showMessageDialog(EyeWitnessDetailCompare.this, header + " can't be blank");
            } else {
                if (header.equals("age")) {
                    int id = Integer.parseInt(data);
                    if (DaoCriminalData.checkAnyDataInt(header, id)) {
                        try {
                            int status = 0;
                            Connection con = DB.getConnection();
                            PreparedStatement ps = con.prepareStatement("select * from CriminalData where " + header + " =?", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                            ps.setInt(1, id);
                            ResultSet rs = ps.executeQuery();
                            rs.beforeFirst();
                            rs.next();

                            if (id == (rs.getInt(header))) {
                                output = rs.getInt(2);
                            }
                        } catch (SQLException ex) {
                            Logger.getLogger(EyeWitnessDetailCompare.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                } else {
                    if (DaoCriminalData.checkAnyDataString(header, data)) {
                        try {
                            int status = 0;
                            Connection con = DB.getConnection();
                            PreparedStatement ps = con.prepareStatement("select * from CriminalData where " + header + " =?", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                            ps.setString(1, data);
                            ResultSet rs = ps.executeQuery();
                            rs.beforeFirst();
                            rs.next();
                            if (data.equals(rs.getString(header))) {
                                output = rs.getInt(2);
                            }
                        } catch (SQLException ex) {
                            Logger.getLogger(EyeWitnessDetailCompare.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
            return output;
        }
    }


